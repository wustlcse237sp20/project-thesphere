package classes;

public class Ticket {

	private String dateAndBand;
	private String seat;
	
	
	public Ticket() {
		
		dateAndBand = this.dateAndBand;
		seat = this.seat;
		
	}
	
	public String getDateAndBand() {
		
		return dateAndBand; 
		
	}
	
	public String getSeat() {
		
		return seat;
		
	}
	
	public void setDateAndBand(String d) {
		
		this.dateAndBand = d;
		
	}
	
	public void setSeat(String s) {
		
		this.seat = s;
		
	}

}
	
